public class Areaofrectangle {
    public static void main(String args[]){
        int l=5;
        int b=10;
        int area=l*b;
        int perimeter1=l+b;
        int perimeter2=2*(perimeter1);
        System.out.println("Area of rectangle is :"+area);
        System.out.println("Perimeter of rectangle is :"+perimeter2);
    }
}
